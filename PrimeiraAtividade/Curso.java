public class Curso
{
    String nome;
    String area;
    int duracao;
    int numerodeAlunos;

    Curso(String nome, String area, int duracao, int  numerodeAlunos)
    {
        this.nome = nome;
        this.area = area;
        this.duracao = duracao;
        this.numerodeAlunos = numerodeAlunos;
    }
}
